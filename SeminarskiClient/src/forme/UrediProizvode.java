    private void btnIzaberiProizvodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiProizvodActionPerformed

        if (tblProizvodi.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Nije izabran proizvod iz tabele");
            return;
        }

        int red = tblProizvodi.getSelectedRow();
        int idProizvoda = (int) tblProizvodi.getValueAt(red, 0);

        List<Proizvod> sviProizvodi = new ArrayList<>();

        try {
            sviProizvodi = Kontroler.getInstance().vratiListuSviProizvodi();
        } catch (Exception ex) {
            System.out.println("Greska pri ucitavanju liste" + ex.getMessage());
        }

        Proizvod izabranProizvod = null;

        for (Proizvod proizvod : sviProizvodi) {
            if (proizvod.getIdProizvod() == idProizvoda) {
                izabranProizvod = proizvod;
            }
        }

        lblID.setText(String.valueOf(izabranProizvod.getIdProizvod()));
        txtNaziv.setText(izabranProizvod.getNaziv());
        txtCena.setText(String.valueOf(izabranProizvod.getJedinicnaCena()));


    }//GEN-LAST:event_btnIzaberiProizvodActionPerformed

