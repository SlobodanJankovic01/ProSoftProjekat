    private boolean validacija() {

        if (txtNaziv.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesi naziv!");
            return false;
        }

        if (!txtCena.getText().matches("^[0-9]+$") || Integer.parseInt(txtCena.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Cena mora biti pozitivan ceo broj veci od 0!", "GreÅ¡ka", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
