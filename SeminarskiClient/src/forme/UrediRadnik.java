    private void btnIzmeniRadnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniRadnikaActionPerformed

        if (lblID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nije izabran radnik");
            return;
        }

        String staraLoznika = String.valueOf(txtStaraLoznika.getPassword());
        int idRadnika = Integer.valueOf(lblID.getText());

        List<Radnik> sviRadnici = new ArrayList<>();
        try {
            sviRadnici = Kontroler.getInstance().vratiListuSviRadnik();
        } catch (Exception ex) {
            System.out.println("Greska pri ucitavanju liste radnika" + ex.getMessage());
        }

        for (Radnik r : sviRadnici) {
            if (r.getIdRadnik() == idRadnika) {
                if (!staraLoznika.equals(r.getLoznika())) {
                    JOptionPane.showMessageDialog(this, "Nije moguce izmeniti radnika, stara loznika nije ispravna");
                    return;
                }
            }
        }

        Radnik r = new Radnik(Integer.valueOf(lblID.getText()), txtIme.getText(), txtPrezime.getText(),
                txtKorIme.getText(), String.valueOf(txtNovaLozinka.getPassword()));

        try {
            Kontroler.getInstance().promeniRadnika(r);

            JOptionPane.showMessageDialog(this, "Uspesno izmenjen radnik");

            popuniTabelu();

            lblID.setText("");
            txtIme.setText("");
            txtPrezime.setText("");
            txtKorIme.setText("");
            txtStaraLoznika.setText("");
            txtNovaLozinka.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspsna izmena radnika");
            System.out.println(ex.getMessage());

        }


    }//GEN-LAST:event_btnIzmeniRadnikaActionPerformed

    private void btnIzaberiRadnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiRadnikaActionPerformed

        if (tblSviRadnici.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Nije izabran radnik iz tabele");
            return;
        }

        int red = tblSviRadnici.getSelectedRow();
        int idRadnik = (int) tblSviRadnici.getValueAt(red, 0);

        List<Radnik> sviRadnici = new ArrayList<>();

        try {
            sviRadnici = Kontroler.getInstance().vratiListuSviRadnik();
        } catch (Exception ex) {
            System.out.println("Greska pri ucitavanju liste radnika" + ex.getMessage());
        }

        Radnik izabranRadnik = null;

        for (Radnik r : sviRadnici) {
            if (r.getIdRadnik() == idRadnik) {
                izabranRadnik = r;
            }
        }

        lblID.setText(String.valueOf(izabranRadnik.getIdRadnik()));
        txtIme.setText(izabranRadnik.getIme());
        txtPrezime.setText(izabranRadnik.getPrezime());
        txtKorIme.setText(izabranRadnik.getKorIme());

    }//GEN-LAST:event_btnIzaberiRadnikaActionPerformed

